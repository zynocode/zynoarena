export const evaluateBoard = (chess: Chess): number => {
  let totalEvaluation = 0;
  const board = chess.board();
  for (let r = 0; r < 8; r++) {
    for (let c = 0; c < 8; c++) {
      const piece = board[r][c];
      if (piece) {
        // Map rank/file to 0-63 index
        let sqIndex = r * 8 + c;
        // PSTs are from white's perspective
        if (piece.color === 'b') {
          const rank = 7 - r;
          sqIndex = rank * 8 + c;
        }
        const val = PIECE_VALUE[piece.type] + PST[piece.type][sqIndex];
        totalEvaluation += piece.color === 'w' ? val : -val;
      }
    }
  }
  return totalEvaluation;
};
